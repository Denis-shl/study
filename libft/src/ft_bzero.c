#include "../includes/libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t		index;
	char		*point;

	index = 0;
	point = (char*)s;
	if (n == 0)
		;
	else
		while (index != n)
		{
			point[index] = '\0';
			index++;
		}
}
