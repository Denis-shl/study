#include "../includes/libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	size_t			index;

	index = 0;
	while (index < len)
	{
		if (src[index] != '\0')
			dst[index] = src[index];
		else
			while (index < len)
			{
				dst[index] = '\0';
				index++;
			}
		index++;
	}
	return (dst);
}
