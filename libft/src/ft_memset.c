#include "../includes/libft.h"

void	*ft_memset(void *b, int c, size_t n)
{
	size_t			i;
	char			a;
	char			*point;

	point = (char *)b;
	a = c;
	i = 0;
	while (i != n)
	{
		point[i] = a;
		i++;
	}
	return (b);
}
